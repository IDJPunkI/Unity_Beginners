1) Is the game too easy or hard? Give 2 unique reasons why it is easy or hard.

I think it's a tad bit too easy. The obstacles are easily avoidable and the powerups are easy to pick up. The phase
ability also doesn't feel as necessary in most cases.


2) What 2 changes would you do to make it easier or harder?

I would change up both the obstacle and powerup speeds to where it picks a speed range to spawn in at random.
I would also add more obstacle types that can rain down from above (camera perspective would have to change) 
or reduce your time. Plus adding wide obstacles that require you to phase through. And as for easier? 
The game honestly doesn't need to be any easier...


3) List 2 things that would give it a "wow" factor.

The landscape, powerup, and obstacles changing as time progresses, music, and sound. Perhaps more ability unlocks
as well as time progresses? Such as freezing time temporarily and/or destroying all obstacles?


4) What 2 parts of the game are fun? 

Phasing through the obstacles at the right time and shifting side to side continually acquiring the powerups.


5) What 2 parts of the game are tedious?

Nothing really tedious about this game since it's relatively simple, but if I had to pick then it's probably keeping 
an eye on the time you have left since your focus is on gathering the powerups and avoiding obstacles.



