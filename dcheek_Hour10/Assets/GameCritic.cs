1) Is the game too easy or hard?

It's easy in the sense you can't lose and how the balls eventually end up in their own goals by themselves, but a little hard with how slow the character moves in my opinion.



2) What would make it easier or harder?

Movement speed increased could make it easier and barriers blocking the way of the goals could make it more difficult.
I also think the goals should only register a score if the player hits the ball in and not the ball going in the goal itself. 



3) What would give it a “wow” factor?

Vfx and sounds added for when a ball enters a goal. Also a theme instead of just colored balls.
Perhaps a horror theme where you have to hit the colored ghosts in while purple ghosts replace the chaos balls and a haunting scream echoes when a goal is made.



4)What parts of the game are fun? What parts of the game are tedious?

I think the slow movement is the tedious part because the balls are simply moving too quick for the player to properly align themselves.
And I find how everything is moving around so quickly and all over the place to be the fun part. 
Truly lives up to the name "Chaos Ball"!



